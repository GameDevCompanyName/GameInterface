package ru.gdcn.beastmaster64revelations.GameInterface.Character.Interactions.Conversation;

public enum DialogType {

    GREETING, COMMON, GOODBYE, TRADE_REQUEST, AGREEMENT, DISAGREEMENT, FIGHT_INITIATION

    /**
     * GREETINGS        - Приветствие. Отправляется при инициации диалога
     *  COMMON           - Обычный ответ
     *  GOODBYE          - Прощание
     *  TRADE_REQUEST    - Запрос на торговлю
     *  AGREEMENT        - Согласие с чем-либо
     *  DISAGREEMENT     - Несогласие с чем-либо
     *  FIGHT_INITIATION - "Нападение", инициация боя с собеседником
     *  **/

}
